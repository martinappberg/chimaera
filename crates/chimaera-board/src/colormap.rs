#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn viridis_endpoints_match_the_published_ramp() {
        let lo = sample("viridis", 0.0).unwrap();
        let hi = sample("viridis", 1.0).unwrap();
        assert_eq!((lo.r, lo.g, lo.b), (68, 1, 84));
        assert_eq!((hi.r, hi.g, hi.b), (253, 231, 37));
    }

    #[test]
    fn viridis_lightness_increases_with_t() {
        // Perceptual uniformity is the point of bundling these; a table that
        // got scrambled in transcription would fail this immediately.
        let l0 = sample("viridis", 0.0).unwrap().luminance();
        let l5 = sample("viridis", 0.5).unwrap().luminance();
        let l1 = sample("viridis", 1.0).unwrap().luminance();
        assert!(l0 < l5 && l5 < l1, "luminance {l0} {l5} {l1}");
    }

    #[test]
    fn every_bundled_name_resolves_and_unknowns_do_not() {
        for name in NAMES {
            assert!(sample(name, 0.5).is_some(), "{name}");
        }
        assert!(sample("rdbu", 0.5).is_some(), "case-insensitive");
        assert!(sample("jet", 0.5).is_none(), "jet stays dead");
    }

    #[test]
    fn t_is_clamped_never_panicking() {
        assert_eq!(
            sample("magma", -3.0).unwrap().hex(),
            sample("magma", 0.0).unwrap().hex()
        );
        assert_eq!(
            sample("magma", 7.0).unwrap().hex(),
            sample("magma", 1.0).unwrap().hex()
        );
        assert_eq!(
            sample("magma", f64::NAN).unwrap().hex(),
            sample("magma", 0.0).unwrap().hex()
        );
    }

    #[test]
    fn rdbu_diverges_red_to_blue() {
        let a = sample("RdBu", 0.0).unwrap();
        let b = sample("RdBu", 1.0).unwrap();
        assert!(a.r > a.b, "low end is red: {}", a.hex());
        assert!(b.b > b.r, "high end is blue: {}", b.hex());
    }
}
