//! Bundled continuous colormaps, referenced by name.
//!
//! A heatmap's colormap is part of what the numbers *mean* — perceptually
//! uniform maps (viridis, magma, cividis) keep equal data steps looking like
//! equal color steps, and the diverging RdBu keeps a meaningful midpoint. So
//! the tables are bundled in the crate and referenced by name, never
//! approximated: a colormap is not expressible as an `a:gradFill`, and letting
//! themes or authors supply arbitrary ramps would reopen every perceptual
//! mistake these four exist to close.
//!
//! Provenance: each table is the exact 256-entry RGB ramp from matplotlib
//! 3.10.9 (`matplotlib.colormaps[name](numpy.linspace(0, 1, 256))`, channels
//! rounded to `round(255 * c)`). viridis and magma are by Stéfan van der Walt
//! and Nathaniel Smith (CC0); cividis is from Nuñez, Anderton & Renslow,
//! PLOS ONE 2018 (optimized for color-vision deficiency); RdBu is Cynthia
//! Brewer's ColorBrewer diverging red–blue (Apache-style license). Literal
//! data rather than polynomial fits, so the ramp is bit-identical to the
//! published reference on every platform.

use crate::theme::Rgb;

/// The bundled colormap names, in the order the docs advertise them.
pub const NAMES: [&str; 4] = ["viridis", "magma", "cividis", "RdBu"];

/// Sample a bundled colormap at `t` in `[0, 1]`.
///
/// `t` is clamped rather than refused — the *caller* owns domain
/// normalization, and an out-of-range sample is a rounding artifact there,
/// not a data error worth losing a render over. Non-finite `t` samples the
/// low end. Unknown names return `None`; the caller names the miss.
pub fn sample(name: &str, t: f64) -> Option<Rgb> {
    let table = table(name)?;
    let t = if t.is_finite() {
        t.clamp(0.0, 1.0)
    } else {
        0.0
    };
    // 256 entries over [0,1]: nearest entry, exactly matplotlib's own
    // `linspace(0, 1, 256)` grid.
    let i = (t * 255.0).round() as usize;
    let [r, g, b] = table[i];
    Some(Rgb { r, g, b })
}

fn table(name: &str) -> Option<&'static [[u8; 3]; 256]> {
    // Case-insensitive so `rdbu` in a hand-typed board still resolves; the
    // canonical spelling stays `RdBu`.
    if name.eq_ignore_ascii_case("viridis") {
        Some(&VIRIDIS)
    } else if name.eq_ignore_ascii_case("magma") {
        Some(&MAGMA)
    } else if name.eq_ignore_ascii_case("cividis") {
        Some(&CIVIDIS)
    } else if name.eq_ignore_ascii_case("rdbu") {
        Some(&RD_BU)
    } else {
        None
    }
}
